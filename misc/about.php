    <h2>The Grand Lodge of Virginia</h2>
    <p>The Mission of the <a href="http://www.grandlodgeofvirginia.org/">Grand Lodge of Virginia</a> is to teach and perpetuate
      a way of life that promotes the Brotherhood of Man under the
      Fatherhood of God and to assist Lodges to grow and prosper.</p>
    
    <p>The Vision of the <a href="http://www.grandlodgeofvirginia.org/">Grand Lodge of Virginia</a> is to be the premier
      organization composed of men of integrity and character, who are
      honest, true to their word, believe in God, are devoted to family,
      charitable in their community, and courteous and helpful to each
      other.</p>
    
    <p>Freemasonry is the oldest Fraternal organization in existence. Written
      records about the Order date back to 1390.  Freemasonry is a
      charitable organization. Masonic organizations in North America
      collectively contribute well over two million dollars every day to
      charities throughout North America.
      
      <h2>Becoming a Freemason</h2>
    <p>In order to become a member of the <a href="http://www.grandlodgeofvirginia.org/">Grand Lodge of Virginia</a> you must
      have two Master Masons vouch for your character and recommend you into
      our Craft.  You also have to be a man, 18 years old or older, of good
      character, and believe in a God that promotes peace, love, and harmony
      towards all mankind.  These are some of the basic requirements for
      membership into our Fraternity.</p>
    
    <p>To become a member of this ancient and honorable Fraternity, a man
      must apply of his own free will and accord and with an open
      heart. Invitations are not issued to prospective members, nor are
      members solicited.  If a person is interested in joining, he should
      ask a Masonic friend for more information or contact
      the <a href="http://www.grandlodgeofvirginia.org/">Grand Lodge of Virginia</a>.</p>
    
    <p>Membership is for men, 18 years of age or older, who meet the
      qualifications and standards of character and reputation, who are of
      good moral character, and who believe in the existence of a supreme
      being.</p>
    
    <p>A man who wants to join a Lodge must be recommended by two Master
      Masons in good standing in any Lodge recognized by
      the <a href="http://www.grandlodgeofvirginia.org/">Grand Lodge of Virginia</a>.
      He must understand that his character will be investigated.
      After approval by the members of that Lodge, he will be accepted as an
      applicant for membership in Freemasonry.</p>
    
    <p>The doors of Freemasonry are open to men who seek harmony with their
      fellowman, feel the need for self-improvement and wish to participate
      in making this world a better place to live.</p>
    
    <p>Any man who becomes a Mason is taught a pattern for living -
      reverence, morality, kindness, honesty, dependability, and
      compassion. He must be prepared to honor his country, uphold its laws,
      and respect those in authority. He must be prepared to maintain
      honorable relations with others and be willing to share in Masonic
      activities.</p>
    
